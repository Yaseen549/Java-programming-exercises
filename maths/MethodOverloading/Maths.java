public class Maths {
    public static int addition(int a, int b) {
        return a+b;
    }
    public static double addition(double a, double b, double c){
        return a+b+c;
    }
    public static int subtraction(int a, int b) {
        return a-b;
    }
    public static double subtraction(double a, double b, double c){
        return a-b-c;
    }
    public static int multiplication(int a, int b) {
        return a*b;
    }
    public static double multiplication(double a, double b, double c){
        return a*b*c;
    }
    public static int division(int a, int b) {
        return a/b;
    }
    public static double division(double a, double b, double c){
        return a/b/c;
    }
    public static int reminder(int a, int b) {
        return a%b;
    }
    public static double reminder(double a, double b, double c){
        return a%b%c;
    }

}
